 	class BaseServiceController extends ActionController {
 		# -------------------------------------------------------
 	
 		# -------------------------------------------------------
 		#
 		# -------------------------------------------------------
 		public function __construct(&$po_request, &$po_response, $pa_view_paths=null) {
 			parent::__construct($po_request, $po_response, $pa_view_paths);
 		}
 		# -------------------------------------------------------
		public function doAuth($ps_username=null,$ps_password=null){
			$va_options = array(
				"no_headers" => true,
				"dont_redirect" => true,
				"options" => array(),
				"user_name" => $ps_username,
				"password" => $ps_password,
			);
			$this->opo_request->doAuthentication($va_options);
			return $this->opo_request->getUserID();
		}
		# -------------------------------------------------------
 	}

